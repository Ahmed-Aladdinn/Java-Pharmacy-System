    public store_order_page(String drugName) {
       initComponents();
       this.setLocationRelativeTo(null);
       Drug_name.setText(drugName);
        Connection con = null;
        PreparedStatement ps = null;
        ResultSet rs = null;
        try { 
            con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy?"
                                            + "user=root&password=root");
            String sql= "Select drugs_name, drugs_quantity, drugs_price from store_drugs where drugs_name=? ";
            ps = con.prepareStatement(sql);
            ps.setString(1, drugName);
            rs = ps.executeQuery();
            if (rs.next()) {
                float price = rs.getFloat("drugs_price");
                this.Drug_price.setText( Float.toString( price ) );
                int quantity = rs.getInt("drugs_quantity");
                SpinnerModel model = new SpinnerNumberModel(1, 1, quantity, 1);
                this.Drug_quantity.setModel(model);
            } 
            else {
                JOptionPane.showMessageDialog(this, "Error connecting to db");
            }
        } 
        catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }
    }

