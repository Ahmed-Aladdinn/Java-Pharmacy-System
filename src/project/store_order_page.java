    private void OrderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_OrderActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy?"
                                               + "user=root&password=root");
            String sql = "insert into pharmacy_orders ( drugs_name, orders_quantity, orders_date)"
                    + " values( ?, ?, ? )";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, Drug_name.getText());
            int drugQuantity = (Integer) Drug_quantity.getValue();
            ps.setInt(2, drugQuantity);
            LocalDateTime todayDatetime = LocalDateTime.now();
            LocalDate todayDate = todayDatetime.toLocalDate();
            ps.setObject(3, todayDate);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully ordered.");
            store_communication page = new store_communication();
            page.setVisible(true);
            this.setVisible(false);
        }
        catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }
    }//GEN-LAST:event_OrderActionPerformed

