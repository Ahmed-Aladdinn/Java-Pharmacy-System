    private void Order_btn2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Order_btn2ActionPerformed
        // TODO add your handling code here:
        try{
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy?"
                                               + "user=root&password=root");
            String sql = "insert into patient_orders (patient_idpatient, drugs_name, orders_quantity, orders_date)"
                    + " values( ?, ?, ?, ? )";
            PreparedStatement ps = con.prepareStatement(sql);
            ps.setString(1, patient_id.getText());
            ps.setString(2, Drug_name2.getText());
            int drugQuantity = (Integer) drug_quantity.getValue();
            ps.setInt(3, drugQuantity);
            LocalDateTime todayDatetime = LocalDateTime.now();
            LocalDate todayDate = todayDatetime.toLocalDate();
            ps.setObject(4, todayDate);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Successfully ordered.");
            Patient_main page = new Patient_main(patient_id.getText());
            page.setVisible(true);
            this.setVisible(false);
        }
        catch (SQLException sqlException) {
                sqlException.printStackTrace();
            }
    }//GEN-LAST:event_Order_btn2ActionPerformed

