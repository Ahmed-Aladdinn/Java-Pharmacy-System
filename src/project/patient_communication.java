    public patient_communication() {
        initComponents();
        this.setLocationRelativeTo(null);
        StoreData = new DefaultTableModel();
        StoreData.addColumn("Order_id");
        StoreData.addColumn("Patient_name");
        StoreData.addColumn("Ordered_drug");
        StoreData.addColumn("Drug_quantity");
        StoreData.addColumn("Order_date");
        
        String patientName = "", orderedDrug;
        Object orderDate;
        int drugQuantity, orderID;
        
        try { 
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy?"
                                            + "user=root&password=root");
            String sql = "select * from patient_orders";
            Statement stat = con.createStatement();
            ResultSet rs = stat.executeQuery(sql);
            while (rs.next()){
                int patientID = rs.getInt("patient_idpatient");
                
                //get patient name from id 
                sql = "select patient_name from patient where idpatient = ? ";
                PreparedStatement ps = con.prepareStatement(sql);
                ps.setInt(1, patientID);
                ResultSet patientSet = ps.executeQuery();
                if (patientSet.next())
                    patientName = patientSet.getString("patient_name");
                else
                    JOptionPane.showMessageDialog(this, "error getting patient name");

                
                orderedDrug = rs.getString("drugs_name");
                drugQuantity = rs.getInt("orders_quantity");
                orderDate = rs.getObject("orders_date");
                orderID = rs.getInt("idorders");
                
                Object[] arr = new Object[]{orderID, patientName, orderedDrug, drugQuantity, orderDate} ;
                StoreData.addRow(arr);
                
            }
        } 
        catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }
        
        Patient_comm_tb.setModel(StoreData);
    }

