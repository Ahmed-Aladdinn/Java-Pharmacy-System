    private void perform_selec_btnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_perform_selec_btnActionPerformed
        // TODO add your handling code here:
        int r =Patient_comm_tb.getSelectedRow();
        
        String  orderIDstr = Patient_comm_tb.getValueAt(r, 0).toString();
        int orderID = Integer.parseInt(orderIDstr);
        
        String orderedDrug = Patient_comm_tb.getValueAt(r, 2).toString();
        
        String drugQuantitystr =  Patient_comm_tb.getValueAt(r, 3).toString();
        int drugQuantity = Integer.parseInt(drugQuantitystr);
        try{
        // 1) decrement quantity in ph_drugs table
            // UPDATE ph_drugs SET drugs_quantity = drugs_quantity-? WHERE drugs_name=? and drugs_quantity > 0 

            Connection c = DriverManager.getConnection("jdbc:mysql://localhost/pharmacy?"
                                        + "user=root&password=root");
            String sql = "UPDATE ph_drugs SET drugs_quantity =drugs_quantity-'"+ drugQuantity +"' WHERE drugs_name= '"+ orderedDrug +"' and drugs_quantity > 0 ";
            Statement stmt = c.createStatement();
            stmt.executeUpdate(sql);
            
            JOptionPane.showMessageDialog(this, "Pharmacy data updated.");
        // 2) delete this row in patient orders table
         // DELETE from patient_orders where idorders = ?
            
            sql = "DELETE from patient_orders where idorders= '"+ orderID +"' ";
            stmt.executeUpdate(sql);
            
            if(Patient_comm_tb.getSelectedRow() != -1) {
               // remove selected row from the model
                StoreData.removeRow(Patient_comm_tb.getSelectedRow());
               JOptionPane.showMessageDialog(null, "Operation done successfully.");
            }
        }
        catch (SQLException sqlException) {
            sqlException.printStackTrace();
        }   
        
    }//GEN-LAST:event_perform_selec_btnActionPerformed

